package day5;

public class forloop {

	public static void main(String[] args) {
		
		for(int r=1;r<=10;r++)
		{
			System.out.println(r);
		}
		
		for(int w=1;w<=10;w++)
			if(w%2==0)
		{
			
			System.out.println("even"+w);
		}
		else 
		{
			System.out.println("odd"+w);
		}

	}

}
